// Composite key: (species << 8) | (is_female << 5) | (variant_index & 0x1F)
static inline uint32_t make_pokemon_large_key(uint16_t species, uint8_t variant_index, uint8_t is_female) {
    return ((uint32_t)species << 8) | ((uint32_t)(is_female & 1) << 5) | (variant_index & 0x1F);
}

const pokemon_large_entry_t* find_pokemon_large(uint16_t species, uint8_t variant_index, uint8_t is_female)
{
    uint32_t search_key = make_pokemon_large_key(species, variant_index, is_female);
    int left = 0;
    int right = POKEMON_LARGE_COUNT - 1;

    while (left <= right)
    {
        int mid = left + (right - left) / 2;
        uint32_t mid_key = pokemon_large_map[mid].composite_key;

        if (mid_key == search_key)
        {
            uint32_t offset = pokemon_large_map[mid].bin_offset;
            uint32_t size = pokemon_large_map[mid].size;

            if (offset + size > POKEMON_LARGE_BIN_SIZE)
            {
            // printf("[COLOR_POKEMON_LARGE_ERROR] Key 0x%06X: bounds check failed (offset=0x%06X + size=%u > BIN_SIZE=%u)\n", search_key, offset, size, POKEMON_LARGE_BIN_SIZE);
                return NULL; // Out of bounds
            }
            // printf("[COLOR_POKEMON_LARGE_FOUND] Key 0x%06X: offset=0x%06X, size=%u bytes, %ux%u pixels\n", search_key, offset, size, pokemon_large_map[mid].width, pokemon_large_map[mid].height);
            // printf("[COLOR_POKEMON_LARGE_FOUND] Species: %u Variant: %u Female: %u\n", species, variant_index, is_female); 
            // return color_pokemon_large + offset;
            return &pokemon_large_map[mid];
        }
        else if (mid_key < search_key) left = mid + 1;
        else right = mid - 1;
    }

    // printf("[COLOR_POKEMON_LARGE_MISS] Key 0x%06X: not found in lookup table\n", search_key);
    return NULL;
}